#include "mathlib.h"
#include <stdlib.h>

int my_add (int num1, int num2) {
    return num1 + num2;
}

int my_sub (int num1, int num2) {
    return num1 - num2;
}

int my_mul (int num1, int num2) {
    return num1 * num2;
}

int my_div (int num1, int num2) {
    return num1 / num2;
}

int my_abs (int num) {
    return abs(num);
}
